import UIKit

extension UIButton {
    
    func buttonUI() {
        let button = self
        button.backgroundColor = #colorLiteral(red: 0.4745098054, green: 0.8392156959, blue: 0.9764705896, alpha: 1)
        button.layer.cornerRadius = 10.0
    }
    
}
